#!/bin/sh
set -e

# review-safety.sh — Check secrets, attribution, conventional commits, sovereignty.
# Usage: review-safety.sh <branch> <workspace>

BRANCH="${1:-}"
WORKSPACE="${2:-.}"
cd "$WORKSPACE"

ok=true

# Check for secrets in diff
for pattern in 'api[_-]?key' 'token' 'secret' 'password' 'private[_-]?key'; do
    if git diff origin/main.."$BRANCH" 2>/dev/null | grep -iE "$pattern" | grep -vE '(example|placeholder|TODO|mock|test|\.md)'; then
        echo "WARN: possible secret in diff matching '$pattern'"
        ok=false
    fi
done

# Check for AI attribution keywords
for pattern in 'Generated by' 'Assisted by' 'Co-Authored-By' 'AI-generated' 'LLM' 'language model'; do
    if git diff origin/main.."$BRANCH" 2>/dev/null | grep -iE "$pattern"; then
        echo "FAIL: AI attribution found in diff matching '$pattern'"
        ok=false
    fi
done

# Check conventional commits
bad_commits=""
bad_commits="$(git log origin/main.."$BRANCH" --pretty=format:"%s" 2>/dev/null | grep -vE '^(feat|fix|docs|style|refactor|test|chore|ci|build|perf)(\(.+\))?!?: .+' | grep -v '^Merge' | grep -v '^v[0-9]+\.' | head -20 || true)"
if [ -n "$bad_commits" ]; then
    echo "FAIL: non-conventional commits found:"
    echo "$bad_commits"
    ok=false
fi

# Check for destructive operations
if git diff origin/main.."$BRANCH" --name-status 2>/dev/null | grep -qE '^D'; then
    echo "WARN: files deleted in this branch"
fi

if [ "$ok" = true ]; then
    echo "PASS: safety"
    exit 0
else
    echo "FAIL: safety"
    exit 1
fi
